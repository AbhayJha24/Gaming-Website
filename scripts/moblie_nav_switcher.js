function switcher(){
	var x; var y;
	x= document.getElementById('mobnavlinks');
	y= document.getElementById('moblinkclose');
		x.style.display="block";
		y.style.display="inline-block";
		}
function destructor(){
	var x; var y;
	x= document.getElementById('mobnavlinks');
		y= document.getElementById('moblinkclose');
		x.style.display="none";
		y.style.display="none";
		}
